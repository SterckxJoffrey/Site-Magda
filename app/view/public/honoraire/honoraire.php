    <section>
      <h2>Rémunération des prestations</h2>
      <p>
        Les honoraires de Maître Tyminska couvrent l’ensemble des prestations juridiques réalisées dans le cadre du traitement d’un dossier. Cela inclut notamment :
      </p>
      <ul>
        <li>L’analyse approfondie du dossier et la constitution des pièces</li>
        <li>La rédaction d’actes de procédure (requêtes, citations, conclusions…)</li>
        <li>Les échanges écrits (courriers, e-mails, SMS) et oraux (consultations, appels)</li>
        <li>Les recherches juridiques en doctrine et jurisprudence</li>
        <li>La représentation devant les juridictions et les déplacements aux audiences</li>
        <li>Les réunions de négociation ou de coordination</li>
      </ul>
      <p>
        Chaque intervention, qu’elle soit visible ou non pour le client, est essentielle à la bonne conduite du dossier.
      </p>
    </section>

    <section>
      <h2>Outre les honoraires, certains frais peuvent s’ajouter :</h2>

      <h3>§ Frais internes</h3>
      <p>Ces frais couvrent les aspects administratifs liés à la gestion du dossier :</p>
      <ul>
        <li>Ouverture et clôture du dossier</li>
        <li>Impression, photocopies, envois postaux ou électroniques</li>
        <li>Constitution de dossiers d’audience, etc.</li>
      </ul>

      <h3>§ Débours et frais de justice</h3>
      <p>Il s’agit des frais avancés pour le compte du client ou à régler directement, tels que :</p>
      <ul>
        <li>Frais d’huissier, de greffe, d’expertise</li>
        <li>Obtention de documents officiels (état civil, registre national…)</li>
        <li>Frais d’expertise</li>
      </ul>
    </section>

    <section class="full-width">
      <h2>Provisions et facturation</h2>
      <p>
        Avant le début de la mission, et éventuellement en cours de traitement, Maître Tyminska peut solliciter une ou plusieurs provisions.
        Ces montants, versés à titre d’avance, seront déduits de l’état final des frais et honoraires.
      </p>
      <p>
        Des états intermédiaires peuvent être transmis au client afin d’assurer un suivi régulier et transparent.
      </p>
