      <ul>
        <li>excès de vitesse, téléphone au volant, non-respect du code,</li>
        <li>retrait ou suspension de permis,</li>
        <li>déchéance du droit de conduire,</li>
        <li>conduite sous l’influence d’alcool ou de stupéfiants,</li>
        <li>responsabilité en cas d’accident de la route.</li>
      </ul>
      <p>Elle vous assiste devant les juridictions compétentes et veille à limiter les conséquences juridiques, financières ou administratives.</p>
