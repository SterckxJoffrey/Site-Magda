<section class="domain">
  <h2>Domaines d'activité</h2>
  <ul>
    <li>
      <h3>Droit de la famille</h3>
      <p>Maître Tyminska accompagne ses clients dans toutes les étapes sensibles liées à la vie familiale :</p>
      <ul>
        <li>procédures de divorce,</li>
        <li>partage des biens et liquidation du régime matrimonial,</li>
        <li>fixation de la garde des enfants, droit de visite et d’hébergement,</li>
        <li>contribution alimentaire et pension alimentaire,</li>
        <li>adoption,</li>
        <li>reconnaissance ou contestation de filiation.</li>
      </ul>
      <p>Elle privilégie une approche humaine et pragmatique, avec une attention particulière à la protection des enfants et à la recherche de solutions équilibrées.</p>
    </li>

    <li>
      <h3>Droit de la jeunesse</h3>
      <p>Maître Tyminska intervient auprès des mineurs impliqués dans des procédures judiciaires, qu’ils soient en conflit avec la loi ou en danger :</p>
      <ul>
        <li>défense des mineurs délinquants devant le tribunal de la jeunesse,</li>
        <li>assistance dans le cadre de mesures de protection (placement, accompagnement éducatif…),</li>
        <li>assistance lors des auditions à la police,</li>
        <li>intervention pour les mineurs en danger,</li>
        <li>dialogue avec les parents, les services sociaux et le Service d’Aide à la Jeunesse (SAJ).</li>
      </ul>
      <p>Elle veille à garantir une défense adaptée à l’âge et à la situation de chaque jeune, dans le respect de son intérêt supérieur.</p>
    </li>

    <li>
      <h3>Droit immobilier</h3>
      <p>Maître Tyminska assiste ses clients dans la gestion et la résolution de leurs litiges immobiliers, qu’ils soient locataires, propriétaires ou syndics :</p>
      <ul>
        <li>loyers impayés et procédures d’expulsion,</li>
        <li>rédaction et analyse de baux d’habitation ou commerciaux,</li>
        <li>conflits entre propriétaires et locataires,</li>
        <li>troubles de voisinage,</li>
        <li>litiges en copropriété (assemblées générales, charges, travaux…).</li>
      </ul>
      <p>Elle intervient aussi bien en phase amiable qu’en contentieux, pour faire valoir vos droits efficacement.</p>
    </li>

    <li>
      <h3> Droit de la circulation routière</h3>
